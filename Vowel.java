//program to find whether a character is vowel?
class Vowel
{
	void alphabets(char alphabet)
	{
		switch (alphabet)
		{case 'a':
		case 'e':
		case 'i':
		case 'o': 
		case 'u':
		System.out.println("It is a Vowel");
		break;
		default:
		System.out.println("It is not a Vowel");
		}
	}
}
